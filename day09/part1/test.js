const solution = require('./solution');
const assert = require('assert');

const input = [
  [ '{<>}', 1 ],
  [ '{<random characters>}', 1 ],
  [ '{<<<<>}', 1 ],
  [ '{<{!>}>', 1 ],
  [ '{<!!>}', 1 ],
  [ '{<!!!>>}', 1 ],
  [ '{<{o"i!a,<{i<a>}', 1 ],
  [ '{<{},{},{{}}>}', 1 ],
  [ '{<a>,<a>,<a>,<a>}', 1 ],
  [ '{{<a>},{<a>},{<a>},{<a>}}', 9 ],
  [ '{{<!>},{<!>},{<!>},{<a>}}', 3 ],
  [ '{}', 1 ],
  [ '{{{}}}', 6 ],
  [ '{{},{}}', 5 ],
  [ '{{{},{},{{}}}}', 16 ],
  [ '{<a>,<a>,<a>,<a>}', 1 ],
  [ '{{<ab>},{<ab>},{<ab>},{<ab>}}', 9 ],
  [ '{{<!!>},{<!!>},{<!!>},{<!!>}}', 9 ],
  [ '{{<a!>},{<a!>},{<a!>},{<ab>}}', 3 ],
];

input.forEach(test => {
  const answer = solution(test[ 0 ]);
  assert(answer === test[ 1 ], `${answer} != ${test[ 1 ]} group: ${test[ 0 ]}`);
});
